"""Deterministic JSON → Markdown sidecar renderer for component result.json files.

Four component renderers + a dispatch function:
  - render_prediction(json_path, md_path)
  - render_learning(json_path, md_path)
  - render_baseline_experiment(json_path, md_path)   # → experiment_name: prediction_no_lessons
  - render_guidance(json_path, md_path)              # guidance runtime artifact; tolerates minimal shape
  - render(component, json_path, md_path)            # dispatch

Every sidecar opens with:
  1. YAML frontmatter — ``autogenerated: true``, ``source: result.json``,
     ``generator: scripts/earnings/result_md_renderer.py``, ``component``,
     ``ticker``, ``quarter``, plus review-surface fields (direction,
     confidence_score, sdk_session_id) and optional ``experiment_name``
     for experiments.
  2. HTML comment block with the ⚠ AUTOGENERATED DO NOT EDIT marker.
  3. Rendered body appropriate to the component.

Determinism invariant: same JSON → same bytes. No timestamps in output
(the data's own predicted_at/attributed_at are rendered, not wall-clock
now). Keys are written in a fixed order.

Per obsidian_thinking.md locked decision §2 this renderer is called
during migration for all 33 historical result.json files AND live
during finalize_*_result() calls for every fresh run.
"""
from __future__ import annotations

import json
from pathlib import Path
from typing import Any


_GENERATOR = "scripts/earnings/result_md_renderer.py"

_READONLY_MARKER = """<!--
⚠ AUTOGENERATED FROM result.json — DO NOT EDIT MANUALLY
Any manual edits will be overwritten on the next finalize_*_result() run
or migration --apply invocation. To change content, update result.json
(via the predictor/learner) or the renderer itself at
scripts/earnings/result_md_renderer.py.
-->"""


def _yaml_scalar(value: Any) -> str:
    """Serialize a scalar value for YAML frontmatter (stable, no Python repr quirks)."""
    if value is None:
        return "null"
    if isinstance(value, bool):
        return "true" if value else "false"
    if isinstance(value, (int, float)):
        return str(value)
    # Strings — if they contain any YAML-sensitive char, quote them.
    text = str(value)
    if text == "" or any(c in text for c in ":#&*!|>%@`\n") or text.strip() != text:
        # JSON string literal is a valid YAML double-quoted scalar.
        return json.dumps(text, ensure_ascii=False)
    return text


def _read_json(json_path: Path) -> dict[str, Any]:
    with open(json_path, "r", encoding="utf-8") as f:
        return json.load(f)


def _session_id_value(payload: dict[str, Any]) -> Any:
    # Treat missing key as null for frontmatter rendering — migration null-stamp
    # writes this key explicitly, but older files may lack it entirely.
    return payload.get("sdk_session_id", None)


def _write_md(md_path: Path, content: str) -> None:
    md_path.parent.mkdir(parents=True, exist_ok=True)
    # Atomic via temp + replace to avoid partial writes.
    tmp = md_path.with_suffix(md_path.suffix + ".tmp")
    tmp.write_text(content, encoding="utf-8")
    tmp.replace(md_path)


# ── Frontmatter + body builders ──────────────────────────────────────────

def _build_frontmatter(
    *,
    component: str,
    ticker: str,
    quarter: str,
    extras: dict[str, Any],
    experiment_name: str | None = None,
) -> str:
    """Produce deterministic YAML frontmatter block.

    Key order is FIXED for byte-determinism:
      autogenerated → source → generator → component → (experiment_name) → ticker → quarter → extras (in insertion order)
    """
    lines = ["---"]
    lines.append(f"autogenerated: true")
    lines.append(f"source: result.json")
    lines.append(f"generator: {_GENERATOR}")
    lines.append(f"component: {_yaml_scalar(component)}")
    if experiment_name is not None:
        lines.append(f"experiment_name: {_yaml_scalar(experiment_name)}")
    lines.append(f"ticker: {_yaml_scalar(ticker)}")
    lines.append(f"quarter: {_yaml_scalar(quarter)}")
    for k, v in extras.items():
        lines.append(f"{k}: {_yaml_scalar(v)}")
    lines.append("---")
    lines.append("")
    return "\n".join(lines)


def _fmt_pct(value: Any) -> str:
    """Format a numeric percent value as +1.23%, or pass-through string."""
    if isinstance(value, (int, float)):
        return f"{value:+.2f}%"
    if value in (None, ""):
        return "n/a"
    return str(value)


def _prediction_body(payload: dict[str, Any], *, is_experiment: bool = False) -> str:
    """Shared body renderer for prediction + experiment (schema ``prediction_result.v1``).

    Real schema (verified 2026-04-17):
      - key_drivers: list[{driver, direction, evidence}]
      - data_gaps:   list[{gap, ...}]
      - evidence_ledger: list[{metric, value, source}]
      - analysis: str
      - expected_move_range_pct: [low, high]
    """
    direction = payload.get("direction", "?")
    conf = payload.get("confidence_score", "?")
    rng = payload.get("expected_move_range_pct") or []
    conf_bucket = payload.get("confidence_bucket", "")
    mag_bucket = payload.get("magnitude_bucket", "")
    model = payload.get("model_version", "")
    predicted_at = payload.get("predicted_at", "")

    ticker = payload.get("ticker", "?")
    quarter = payload.get("quarter_label", "?")

    lines = []
    heading = "Prediction" if not is_experiment else "Prediction (no_lessons experiment)"
    lines.append(f"# {heading} — {ticker} {quarter}")
    lines.append("")
    lines.append(f"**Direction**: {direction} @ confidence {conf}")
    if rng and isinstance(rng, list) and len(rng) == 2:
        lines.append(f"**Expected move**: {rng[0]} – {rng[1]}%")
    if conf_bucket or mag_bucket:
        lines.append(f"**Buckets**: confidence={conf_bucket or '?'}  magnitude={mag_bucket or '?'}")
    if model:
        lines.append(f"**Model**: {model}")
    if predicted_at:
        lines.append(f"**Predicted at**: {predicted_at}")
    lines.append("")

    drivers = payload.get("key_drivers") or []
    if drivers:
        lines.append("## Key Drivers")
        lines.append("")
        for i, d in enumerate(drivers, 1):
            if isinstance(d, dict):
                # Real shape: {driver, direction, evidence}
                driver_text = d.get("driver") or d.get("factor") or d.get("summary") or "?"
                dir_tag = d.get("direction") or ""
                evidence = d.get("evidence") or ""
                header = f"{i}. **{driver_text}**"
                if dir_tag:
                    header += f"  `{dir_tag}`"
                lines.append(header)
                if evidence:
                    lines.append(f"    - _evidence:_ {evidence}")
            else:
                lines.append(f"{i}. {d}")
        lines.append("")

    analysis = payload.get("analysis", "") or ""
    if analysis:
        lines.append("## Analysis")
        lines.append("")
        lines.append(analysis)
        lines.append("")

    gaps = payload.get("data_gaps") or []
    if gaps:
        lines.append(f"## Data Gaps ({len(gaps)})")
        lines.append("")
        for g in gaps:
            if isinstance(g, dict):
                # Real shape: {gap, ...} or {gap, impact, mitigation}
                gap_text = g.get("gap") or g.get("description") or g.get("summary") or "?"
                lines.append(f"- {gap_text}")
                for extra_key in ("impact", "mitigation"):
                    extra = g.get(extra_key)
                    if extra:
                        lines.append(f"    - _{extra_key}:_ {extra}")
            else:
                lines.append(f"- {g}")
        lines.append("")

    ledger = payload.get("evidence_ledger") or []
    if ledger:
        lines.append(f"## Evidence Ledger ({len(ledger)})")
        lines.append("")
        lines.append("| # | Metric | Value | Source |")
        lines.append("|---|---|---|---|")
        for i, e in enumerate(ledger, 1):
            if isinstance(e, dict):
                metric = str(e.get("metric") or e.get("id") or "?").replace("|", "\\|")
                value = str(e.get("value") or "").replace("|", "\\|").replace("\n", " ")
                source = str(e.get("source") or "").replace("|", "\\|").replace("\n", " ")
                lines.append(f"| {i} | {metric} | {value} | {source} |")
        lines.append("")

    return "\n".join(lines)


def _learning_body(payload: dict[str, Any]) -> str:
    """Learning body renderer (schema ``attribution_result.v2``).

    Real schema (verified 2026-04-17):
      - actual_return: {daily_stock_pct, hourly_stock_pct, session_stock_pct,
                        daily_macro_pct, daily_sector_pct, daily_industry_pct,
                        market_session}
      - primary_driver: {summary, category, evidence_refs}
      - contributing_factors: list[{summary, ...}]
      - feedback: {prediction_comparison{...}, what_worked[], what_failed[],
                   why, predictor_lessons[], data_lessons[]}
      - evidence_ledger: list[{id, claim, value, source, date}]
      - global_observations: list[{scope, target_sector|related_tickers, lesson}]
      - missing_inputs, data_sources_used: list[str]
      - pit_mode, pit_cutoff, pit_boundary_source, filed_8k, accession_8k: str
    """
    ticker = payload.get("ticker", "?")
    quarter = payload.get("quarter_label", "?")
    primary = payload.get("primary_driver", {}) or {}
    actual = payload.get("actual_return", {}) or {}
    feedback = payload.get("feedback", {}) or {}

    lines = []
    lines.append(f"# Learning — {ticker} {quarter}")
    lines.append("")

    # ── Header metadata ─────────────────────────────────────────────
    model = payload.get("model_version", "")
    attributed_at = payload.get("attributed_at", "")
    filed_8k = payload.get("filed_8k", "")
    accession = payload.get("accession_8k", "")
    if model:
        lines.append(f"**Model**: {model}")
    if attributed_at:
        lines.append(f"**Attributed at**: {attributed_at}")
    if filed_8k:
        lines.append(f"**Filed 8-K**: {filed_8k}")
    if accession:
        lines.append(f"**Accession**: `{accession}`")
    lines.append("")

    # ── PIT block ────────────────────────────────────────────────────
    pit_mode = payload.get("pit_mode", "")
    pit_cutoff = payload.get("pit_cutoff", "")
    pit_source = payload.get("pit_boundary_source", "")
    if pit_mode or pit_cutoff or pit_source:
        lines.append(f"**PIT**: mode={pit_mode or '?'}  cutoff={pit_cutoff or '?'}  boundary_source={pit_source or '?'}")
        lines.append("")

    # ── Actual returns ──────────────────────────────────────────────
    if actual:
        lines.append("## Actual Returns")
        lines.append("")
        lines.append("| Series | Value |")
        lines.append("|---|---|")
        for k in ("daily_stock_pct", "hourly_stock_pct", "session_stock_pct",
                  "daily_macro_pct", "daily_sector_pct", "daily_industry_pct"):
            if k in actual:
                lines.append(f"| {k} | {_fmt_pct(actual.get(k))} |")
        mkt = actual.get("market_session")
        if mkt:
            lines.append(f"| market_session | {mkt} |")
        lines.append("")

    # ── Primary driver ──────────────────────────────────────────────
    if primary:
        lines.append("## Primary Driver")
        lines.append("")
        if isinstance(primary, dict):
            cat = primary.get("category", "")
            summary = primary.get("summary", "")
            refs = primary.get("evidence_refs", []) or []
            if cat:
                lines.append(f"**Category**: `{cat}`")
            if summary:
                lines.append("")
                lines.append(summary)
            if refs:
                refs_str = ", ".join(f"`{r}`" for r in refs)
                lines.append("")
                lines.append(f"_Evidence refs:_ {refs_str}")
        else:
            # Fallback: legacy string-shaped primary_driver
            lines.append(str(primary))
        lines.append("")

    # ── Contributing factors ────────────────────────────────────────
    contributing = payload.get("contributing_factors") or []
    if contributing:
        lines.append(f"## Contributing Factors ({len(contributing)})")
        lines.append("")
        for i, c in enumerate(contributing, 1):
            if isinstance(c, dict):
                summary = c.get("summary") or c.get("factor") or "?"
                cat = c.get("category") or ""
                refs = c.get("evidence_refs") or []
                header = f"{i}. {summary}"
                if cat:
                    header += f"  (category: `{cat}`)"
                lines.append(header)
                if refs:
                    lines.append(f"    - _refs:_ {', '.join(f'`{r}`' for r in refs)}")
            else:
                lines.append(f"{i}. {c}")
        lines.append("")

    # ── Feedback (rich structure) ───────────────────────────────────
    if feedback:
        lines.append("## Feedback")
        lines.append("")
        comparison = feedback.get("prediction_comparison", {}) or {}
        if comparison:
            lines.append("### Prediction vs actual")
            lines.append("")
            for k in ("predicted_direction", "predicted_confidence_score",
                      "predicted_move_range_pct", "predicted_key_drivers",
                      "direction_correct", "magnitude_error_pct"):
                if k in comparison:
                    v = comparison[k]
                    if isinstance(v, list):
                        v = json.dumps(v, ensure_ascii=False)
                    lines.append(f"- **{k}**: {v}")
            lines.append("")
        why = feedback.get("why", "")
        if why:
            lines.append("### Why (causal explanation)")
            lines.append("")
            lines.append(why)
            lines.append("")
        for section_key, heading in [
            ("what_worked", "What worked"),
            ("what_failed", "What failed"),
            ("predictor_lessons", "Predictor lessons"),
            ("data_lessons", "Data lessons"),
        ]:
            items = feedback.get(section_key) or []
            if items:
                lines.append(f"### {heading}")
                lines.append("")
                for item in items:
                    if isinstance(item, dict):
                        lines.append(f"- {json.dumps(item, ensure_ascii=False)}")
                    else:
                        lines.append(f"- {item}")
                lines.append("")

    # ── Global observations ─────────────────────────────────────────
    observations = payload.get("global_observations") or []
    if observations:
        lines.append(f"## Global Observations ({len(observations)})")
        lines.append("")
        for obs in observations:
            if isinstance(obs, dict):
                scope = obs.get("scope", "?")
                lesson = obs.get("lesson", "")
                route_bits = []
                if obs.get("target_sector"):
                    route_bits.append(f"target_sector={obs['target_sector']}")
                if obs.get("related_tickers"):
                    rt = obs["related_tickers"]
                    if isinstance(rt, list):
                        rt = ",".join(rt)
                    route_bits.append(f"related_tickers=[{rt}]")
                route = f" ({'; '.join(route_bits)})" if route_bits else ""
                lines.append(f"- **[{scope}{route}]** {lesson}")
        lines.append("")

    # ── Evidence ledger ─────────────────────────────────────────────
    ledger = payload.get("evidence_ledger") or []
    if ledger:
        lines.append(f"## Evidence Ledger ({len(ledger)})")
        lines.append("")
        lines.append("| ID | Claim | Value | Source | Date |")
        lines.append("|---|---|---|---|---|")
        for e in ledger:
            if isinstance(e, dict):
                eid = str(e.get("id") or "?").replace("|", "\\|")
                claim = str(e.get("claim") or "").replace("|", "\\|").replace("\n", " ")
                value = str(e.get("value") or "").replace("|", "\\|").replace("\n", " ")
                source = str(e.get("source") or "").replace("|", "\\|").replace("\n", " ")
                date = str(e.get("date") or "")
                lines.append(f"| {eid} | {claim} | {value} | {source} | {date} |")
        lines.append("")

    # ── Missing inputs + data sources ───────────────────────────────
    missing = payload.get("missing_inputs") or []
    if missing:
        lines.append(f"## Missing Inputs ({len(missing)})")
        lines.append("")
        for m in missing:
            lines.append(f"- {m}")
        lines.append("")

    sources = payload.get("data_sources_used") or []
    if sources:
        lines.append("## Data Sources Used")
        lines.append("")
        for s in sources:
            lines.append(f"- `{s}`")
        lines.append("")

    return "\n".join(lines)


def _guidance_body(payload: dict[str, Any]) -> str:
    ticker = payload.get("ticker", "?")
    quarter = payload.get("quarter_label", "?")
    source_id = payload.get("source_id", "")
    period = payload.get("period_of_report", "")
    summary = payload.get("summary", "") or ""

    lines = []
    lines.append(f"# Guidance — {ticker} {quarter}")
    lines.append("")
    if source_id:
        lines.append(f"**Source**: `{source_id}`")
    if period:
        lines.append(f"**Period of report**: {period}")
    lines.append("")
    if summary:
        lines.append("## Summary")
        lines.append("")
        lines.append(summary)
        lines.append("")
    return "\n".join(lines)


# ── Public render functions ──────────────────────────────────────────────

def render_prediction(json_path: Path, md_path: Path) -> None:
    """Render a prediction result.json to its result.md sidecar."""
    payload = _read_json(json_path)
    extras = {
        "direction": payload.get("direction", ""),
        "confidence_score": payload.get("confidence_score", ""),
        "sdk_session_id": _session_id_value(payload),
    }
    fm = _build_frontmatter(
        component="prediction",
        ticker=str(payload.get("ticker", "")),
        quarter=str(payload.get("quarter_label", "")),
        extras=extras,
    )
    body = _prediction_body(payload, is_experiment=False)
    _write_md(md_path, fm + _READONLY_MARKER + "\n\n" + body + "\n")


def render_learning(json_path: Path, md_path: Path) -> None:
    """Render a learning result.json to its result.md sidecar.

    Note: schema_version string stays as ``attribution_result.v2`` (schema
    names are NOT renamed — only the folder is). Frontmatter exposes it
    so Dataview can filter.
    """
    payload = _read_json(json_path)
    extras = {
        "schema_version": payload.get("schema_version", ""),
        "sdk_session_id": _session_id_value(payload),
    }
    fm = _build_frontmatter(
        component="learning",
        ticker=str(payload.get("ticker", "")),
        quarter=str(payload.get("quarter_label", "")),
        extras=extras,
    )
    body = _learning_body(payload)
    _write_md(md_path, fm + _READONLY_MARKER + "\n\n" + body + "\n")


def render_baseline_experiment(json_path: Path, md_path: Path) -> None:
    """Render a no_lessons baseline experiment result.json.

    Frontmatter: ``component: prediction`` (PARENT) + ``experiment_name:
    prediction_no_lessons`` — per obsidian_thinking.md locked experiment
    frontmatter rule (lets Dataview filter "all prediction artifacts
    including experiments" via ``component: prediction``).
    """
    payload = _read_json(json_path)
    extras = {
        "direction": payload.get("direction", ""),
        "confidence_score": payload.get("confidence_score", ""),
        "sdk_session_id": _session_id_value(payload),
    }
    fm = _build_frontmatter(
        component="prediction",
        ticker=str(payload.get("ticker", "")),
        quarter=str(payload.get("quarter_label", "")),
        extras=extras,
        experiment_name="prediction_no_lessons",
    )
    body = _prediction_body(payload, is_experiment=True)
    _write_md(md_path, fm + _READONLY_MARKER + "\n\n" + body + "\n")


def render_guidance(json_path: Path, md_path: Path) -> None:
    """Render a guidance result.json (K8s worker output, minimal shape tolerated)."""
    payload = _read_json(json_path)
    extras = {
        "sdk_session_id": _session_id_value(payload),
    }
    fm = _build_frontmatter(
        component="guidance",
        ticker=str(payload.get("ticker", "")),
        quarter=str(payload.get("quarter_label", "")),
        extras=extras,
    )
    body = _guidance_body(payload)
    _write_md(md_path, fm + _READONLY_MARKER + "\n\n" + body + "\n")


def render(component: str, json_path: Path, md_path: Path) -> None:
    """Dispatch to the correct render function by component name.

    Accepts: "prediction", "learning", "guidance", or the experiment tag
    "prediction_no_lessons".

    Raises:
        ValueError: if ``component`` is not one of the recognized values.
    """
    dispatch = {
        "prediction": render_prediction,
        "learning": render_learning,
        "guidance": render_guidance,
        "prediction_no_lessons": render_baseline_experiment,
    }
    if component not in dispatch:
        raise ValueError(
            f"unknown component {component!r}; "
            f"must be one of {sorted(dispatch.keys())}"
        )
    dispatch[component](json_path, md_path)
